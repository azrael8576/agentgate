def _inject_nonblocking_inherited_control_failure(output_dir: Path) -> None:
    def update_verification(payload: dict[str, Any]) -> None:
        payload["results"].append(
            {
                "gate_id": "technical_tool_success_rate_within_threshold",
                "control_title": "Technical tool success rate must stay within threshold.",
                "verification_status": "FAIL",
                "blocking": False,
                "matched_metric_ids": ["technical_tool_success_rate"],
                "candidate_values": {"technical_tool_success_rate": 0.5},
            }
        )
        summary = payload.setdefault("summary", {})
        summary["failed"] = int(summary.get("failed", 0)) + 1
        summary["passed"] = max(0, int(summary.get("passed", 0)) - 1)

    _rewrite_json(output_dir / "control_verification_results.json", update_verification)

    def update_decision(payload: dict[str, Any]) -> None:
        future = payload.setdefault("future_verification", {})
        future["failed"] = int(future.get("failed", 0)) + 1

    _rewrite_json(output_dir / "release_decision.json", update_decision)


