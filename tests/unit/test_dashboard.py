    monkeypatch.setattr(
        routes_dashboard.release_check_module,
        "run_release_check",
        fake_run_release_check,
    )
    client = TestClient(app)

    response = client.post(
        "/api/agentgate/release-check",
        json={
            "source": "local",
            "evidence": str(evidence),
            "agent_version": "v2.1",
            "release_controls_ref": str(controls),
        },
    )

    assert response.status_code == 200
    payload = response.json()
    assert payload["future_verification"]["status"] == "verified"
    assert payload["future_verification"]["total_controls"] == 4
    assert payload["future_verification"]["passed"] == 4
