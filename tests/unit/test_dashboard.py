def _seed(version: str, tmp_path: Path) -> Path:
    output = tmp_path / f"seed_{version.replace('.', '')}_evidence.jsonl"
    write_seed_evidence(version, output)
    return output


def _write_artifacts(version: str, target: Path, tmp_path: Path) -> None:
    run_release_check(_seed(version, tmp_path), target)


def _rewrite_json(path: Path, update: Any) -> None:
    payload = json.loads(path.read_text(encoding="utf-8"))
    update(payload)
    path.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")


def _mark_warning_only_failure(output_dir: Path) -> None:
    metrics_path = output_dir / "metrics_summary.json"

    def update(payload: dict[str, Any]) -> None:
        for metric in payload["metrics"]:
            if metric.get("decision_impact") != "warning":
                continue
            if metric["name"] == "intent_routing_accuracy":
                metric["value"] = 0.91
                metric["passes_threshold"] = False
            else:
                metric["passes_threshold"] = True

    _rewrite_json(metrics_path, update)


