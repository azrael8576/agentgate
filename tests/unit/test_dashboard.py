def test_release_check_api_passes_release_controls_ref(tmp_path: Path, monkeypatch: Any) -> None:
    latest_dir = tmp_path / "latest"
    evidence = _seed("v2.1", tmp_path)
    controls = tmp_path / "regression_gates.json"
    shutil.copy(
        Path(__file__).resolve().parents[2]
        / "examples"
        / "artifacts"
        / "reference-v2"
        / "regression_gates.json",
        controls,
    )
    calls: list[dict[str, Any]] = []
    real_run_release_check = release_check_module.run_release_check

    def fake_run_release_check(**kwargs: Any) -> dict[str, Any]:
        calls.append(kwargs)
        return real_run_release_check(
            evidence_path=kwargs["evidence_path"],
            output_dir=kwargs["output_dir"],
            diagnosis_mode=kwargs["diagnosis_mode"],
            release_config=kwargs["release_config"],
        )

